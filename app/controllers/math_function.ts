export const addFn = (val1: number, val2: number) => {
  return val1 + val2;
};

export const minusFn = (val1: number, val2: number) => {
  return val1 - val2;
};

export const multiplyFn = (val1: number, val2: number) => {
  return val1 * val2;
};

export const divideFn = (val1: number, val2: number) => {
  return val1 / val2;
};
